{
    public enum MusicalNotes
    {
        A,              // fa
        ASharp = 1,     // fa sustenido
        BBemol = 1,     // si bemol
        B,              // si
        C,              // do
        CSharp = 4,     // do sustenido
        DBemol = 4,     // re bemol
        D,              // re
        DSharp = 6,     // re sustenido
        EFlat = 6,      // mi bemol
        E,              // mi
        F,              // fa
        FSharp = 9,     // fa sustenido
        GFlat = 9,      // sol bemol
        G,              // sol
        GSharp = 11,    // sol sustenido 
        AFlat = 11,     // fa bemol
    }
}